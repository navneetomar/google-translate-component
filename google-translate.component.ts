  constructor(private renderer: Renderer2) {}

  ngOnInit() {
    this.loadGoogleTranslateScript();
    this.hideGoogleToolbar();
    this.setCurrentLanguageFromCookie();  // <--- ye line add ki hai
  }

  loadGoogleTranslateScript() {
    if (!(window as any).googleTranslateElementInit) {
      (window as any).googleTranslateElementInit = () => {
        new (window as any).google.translate.TranslateElement({
          pageLanguage: 'en',
          includedLanguages: this.languages.map(l => l.code).join(','),
          layout: (window as any).google.translate.TranslateElement.InlineLayout.HORIZONTAL,
          autoDisplay: false
        }, 'google_translate_element');
      };
    }

    if (!document.getElementById('google-translate-script')) {
      const script = document.createElement('script');
      script.id = 'google-translate-script';
      script.src = `//translate.google.com/translate_a/element.js?cb=googleTranslateElementInit`;
      document.body.appendChild(script);
    }
  }

  changeLanguage(lang: string) {
    this.currentLang = lang;

    if (lang === 'en') {
      // English par aate hi translation hatani hai
      document.cookie = `googtrans=/en/en; path=/`;
      window.location.reload();
    } else {
      document.cookie = `googtrans=/en/${lang}; path=/`;
      window.location.reload();
    }
  }

  setCurrentLanguageFromCookie() {
    const match = document.cookie.match(/googtrans=\/en\/(\w+)/);
    if (match && match[1] && match[1] !== 'en') {
      this.currentLang = match[1];
    } else {
      this.currentLang = '';
    }
  }

  hideGoogleToolbar() {
    const style = document.createElement('style');
    style.innerHTML = `
      .goog-te-banner-frame, 
      .goog-te-menu-frame, 
      .goog-te-ftab-frame, 
      .skiptranslate, 
      .goog-te-gadget {
        display: none !important;
        visibility: hidden !important;
        height: 0 !important;
        width: 0 !important;
        overflow: hidden !important;
        position: absolute !important;
      }
      body {
        top: 0 !important;
      }
      .goog-te-combo {
        display: none !important;
      }
    `;
    document.head.appendChild(style);

    const observer = new MutationObserver(() => {
      const elements = document.querySelectorAll('.goog-te-banner-frame, .skiptranslate');
      elements.forEach(el => {
        this.renderer.setStyle(el, 'display', 'none');
      });
    });

    observer.observe(document.body, {
      childList: true,
      subtree: true
    });
  }
